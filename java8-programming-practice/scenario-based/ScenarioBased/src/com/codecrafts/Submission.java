package com.codecrafts;

public class Submission {
	String userId;
	String projectCode;
	int durationInMinutes;
	int score;
	
	public Submission(String userId, String projectCode, int durationInMinutes, int score) {
		this.userId = userId;
		this.projectCode = projectCode;
		this.durationInMinutes = durationInMinutes;
		this.score = score;
	}
	
	

}
