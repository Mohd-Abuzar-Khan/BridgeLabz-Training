package sorting.fleetmanager;

public class Vehicle {
	
	String brand;
	String model;
	int mileage;
	String depot;
	public Vehicle(String brand, String model, int mileage, String depot) {
		super();
		this.brand = brand;
		this.model = model;
		this.mileage = mileage;
		this.depot = depot;
	}
	
	
}
